    private void btnConfirmar_cadastroFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmar_cadastroFuncionarioActionPerformed
        //diz se o cadastro pode ou nÃ£o ser feito
        boolean cadastro = true;
        //identifica se estÃ¡ sendo cadastrado um funcionÃ¡rio ou um gerente
        //se o nome nÃ£o foi escrito o cadastro nÃ£o pode ser realizado
        if (nome_cadastroFuncionario.getText().equals("") || nome_cadastroFuncionario.getText().equals("Campo obrigatÃ³rio")) {
            nome_cadastroFuncionario.setText("Campo obrigatÃ³rio");
            cadastro = false;
        }
        //verifica se algum campo referente ao endereÃ§o foi cadastrado, se um deles for cadastrado, todos devem ser cadastrados
        if (!(cidade_cadastroFuncionario.getText().equals("") && rua_cadastroFuncionario.getText().equals("") && bairro_cadastroFuncionario.getText().equals("") && numero_cadastroFuncionario.getText().equals("") && uf_cadastroFuncionario.getText().equals("") && cep_cadastroFuncionario.getText().equals(""))) {
            if (cidade_cadastroFuncionario.getText().equals("") || cidade_cadastroFuncionario.getText().equals("Campo obrigatÃ³rio se for cadastrar o endereÃ§o")) {
                cidade_cadastroFuncionario.setText("Campo obrigatÃ³rio se for cadastrar o endereÃ§o");
                cadastro = false;
            }
            if (bairro_cadastroFuncionario.getText().equals("") || bairro_cadastroFuncionario.getText().equals("Campo obrigatÃ³rio se for cadastrar o endereÃ§o")) {
                bairro_cadastroFuncionario.setText("Campo obrigatÃ³rio se for cadastrar o endereÃ§o");
                cadastro = false;
            }
            if (rua_cadastroFuncionario.getText().equals("") || rua_cadastroFuncionario.getText().equals("Campo obrigatÃ³rio se for cadastrar o endereÃ§o")) {
                rua_cadastroFuncionario.setText("Campo obrigatÃ³rio se for cadastrar o endereÃ§o");
                cadastro = false;
            }
            if (uf_cadastroFuncionario.getText().equals("") || uf_cadastroFuncionario.getText().equals("Campo obrigatÃ³rio se for cadastrar o endereÃ§o")) {
                uf_cadastroFuncionario.setText("Campo obrigatÃ³rio se for cadastrar o endereÃ§o");
                cadastro = false;
            }
            if (numero_cadastroFuncionario.getText().equals("") || numero_cadastroFuncionario.getText().equals("Campo obrigatÃ³rio se for cadastrar o endereÃ§o")) {
                numero_cadastroFuncionario.setText("Campo obrigatÃ³rio se for cadastrar o endereÃ§o");
                cadastro = false;
            }
            if (cep_cadastroFuncionario.getText().equals("") || cep_cadastroFuncionario.getText().equals("Campo obrigatÃ³rio se for cadastrar o endereÃ§o")) {
                cep_cadastroFuncionario.setText("Campo obrigatÃ³rio se for cadastrar o endereÃ§o");
                cadastro = false;
            }
            if (cadastro) {
                //com todos os campos de endereÃ§o preenchidos, o cadastro serÃ¡ feito com o endereÃ§o
                Endereco endereco = new Endereco(cidade_cadastroFuncionario.getText(), cep_cadastroFuncionario.getText(), uf_cadastroFuncionario.getText(), bairro_cadastroFuncionario.getText(), rua_cadastroFuncionario.getText(), numero_cadastroFuncionario.getText());
                new Funcionario(nome_cadastroFuncionario.getText(), endereco, "madeLavant", "A");
            }
        } else {
            //se nenhum campo de endereÃ§o estiver cadastrado, o cadastro serÃ¡ feito sem o endereÃ§o
            if (cadastro) {
                new Funcionario(nome_cadastroFuncionario.getText(), "madeLavant", "A");
            }
        }
        //retorna para a tela de crud de funcionÃ¡rios se o cadastro foi realizado
        if (cadastro) {
            this.setVisible(false);
            new InicioGerente().setVisible(true);
        }
    }//GEN-LAST:event_btnConfirmar_cadastroFuncionarioActionPerformed

